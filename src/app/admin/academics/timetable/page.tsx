      !!selectedSection && selectedDays.some(day => (timetable[day] || []).some(item => !item.is_break));
    if (hasExistingData && !overwriteExisting) {
      showConfirmDialog(
        'Overwrite Existing Timetable',
        `Timetable entries already exist in selected day(s). Do you want to replace them with an auto-generated timetable?`,
        () => handleAutoGenerateTimetable(true),
        'warning',
        'Overwrite'
      );
      return;
    }

    try {
      setLoading(prev => ({ ...prev, autoGenerate: true }));

      const payload: {
        class_name: string;
        section?: string;
        sections?: string[];
        days: string[];
        day_period_counts: Record<string, number>;
        period_duration_minutes: number;
        first_period_start_time: string;
        max_subject_periods_per_day: number;
        support_combined_class?: boolean;
        subject_periods: Record<string, number>;
        section_subject_teachers?: Record<string, Record<string, string>>;
        overwrite_existing: boolean;
      } = {
        class_name: selectedClass,
        days: selectedDays,
        day_period_counts: dayPeriodCounts,
        period_duration_minutes: duration,
        first_period_start_time: `${autoGenerateFirstPeriodStartTime}:00`,
        max_subject_periods_per_day: Number(autoGenerateMaxSubjectPerDay) || 2,
        support_combined_class: autoGenerateSupportCombinedClass,
        subject_periods: subjectPeriods,
        overwrite_existing: overwriteExisting
      };

      if (autoGenerateSections.length === 1) {
        payload.section = autoGenerateSections[0];
      } else {
        payload.sections = autoGenerateSections;
      }

      const sectionSubjectTeachers: Record<string, Record<string, string>> = {};
      autoGenerateSections.forEach((sectionName) => {
        const subjectMap: Record<string, string> = {};
        subjects.forEach((subject) => {
          const teacherId = autoGenerateSectionTeachers?.[sectionName]?.[subject.name];
          if (teacherId) {
            subjectMap[subject.name] = teacherId;
          }
        });
        if (Object.keys(subjectMap).length > 0) {
          sectionSubjectTeachers[sectionName] = subjectMap;
        }
      });
      if (Object.keys(sectionSubjectTeachers).length > 0) {
        payload.section_subject_teachers = sectionSubjectTeachers;
      }

      const response = await adminApi.timetable.autoGenerate({
        ...payload,
        ...schoolScope.scopeParams,
      });

      toastSuccess(
        response.data?.message ||
          `Auto-generated timetable for ${autoGenerateSections.join(', ')}`
      );
      setShowAutoGenerateModal(false);
      setViewMode('view');
      fetchTimetable();
    } catch (error: any) {
      const data = error?.response?.data;
      const extractError = (err: any): string => {
        if (!err) return 'Failed to auto-generate timetable';
        if (typeof err === 'string') return err;
        if (err.error && typeof err.error === 'string') return err.error;
        if (Array.isArray(err)) return err.join(', ');
        if (typeof err === 'object') {
          const values = Object.values(err).flatMap((v: any) =>
            Array.isArray(v) ? v : [typeof v === 'string' ? v : JSON.stringify(v)]
          );
          if (values.length > 0) return values.join(', ');
        }
        return 'Failed to auto-generate timetable';
      };
      const backendError = extractError(data);
      const shouldPromptOverwrite =
        !overwriteExisting &&
        typeof backendError === 'string' &&
        backendError.toLowerCase().includes('enable overwrite');

      if (shouldPromptOverwrite) {
        showConfirmDialog(
          'Overwrite Existing Timetable',
          backendError,
          () => handleAutoGenerateTimetable(true),
          'warning',
          'Overwrite'
        );
        return;
      }

      toastError(backendError);
    } finally {
      setLoading(prev => ({ ...prev, autoGenerate: false }));
    }
